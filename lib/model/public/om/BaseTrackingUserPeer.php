	/**
	 * Selects a collection of TrackingUser objects pre-filled with their Contacto objects.
	 *
	 * @return     array Array of TrackingUser objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinContacto(Criteria $c, $con = null)
	{
		$c = clone $c;

		// Set the correct dbName if it has not been overridden
		if ($c->getDbName() == Propel::getDefaultDB()) {
			$c->setDbName(self::DATABASE_NAME);
		}

		TrackingUserPeer::addSelectColumns($c);
		$startcol = (TrackingUserPeer::NUM_COLUMNS - TrackingUserPeer::NUM_LAZY_LOAD_COLUMNS) + 1;
		ContactoPeer::addSelectColumns($c);

		$c->addJoin(TrackingUserPeer::CA_IDCONTACTO, ContactoPeer::CA_IDCONTACTO);
		$rs = BasePeer::doSelect($c, $con);
		$results = array();

		while($rs->next()) {

			$omClass = TrackingUserPeer::getOMClass();

			$cls = Propel::import($omClass);
			$obj1 = new $cls();
			$obj1->hydrate($rs);

			$omClass = ContactoPeer::getOMClass();

			$cls = Propel::import($omClass);
			$obj2 = new $cls();
			$obj2->hydrate($rs, $startcol);

			$newObject = true;
			foreach($results as $temp_obj1) {
				$temp_obj2 = $temp_obj1->getContacto(); //CHECKME
				if ($temp_obj2->getPrimaryKey() === $obj2->getPrimaryKey()) {
					$newObject = false;
					// e.g. $author->addBookRelatedByBookId()
					$temp_obj2->addTrackingUser($obj1); //CHECKME
					break;
				}
			}
			if ($newObject) {
				$obj2->initTrackingUsers();
				$obj2->addTrackingUser($obj1); //CHECKME
			}
			$results[] = $obj1;
		}
		return $results;
	}


